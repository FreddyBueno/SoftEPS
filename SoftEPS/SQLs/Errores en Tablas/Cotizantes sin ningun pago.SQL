SELECT TIP_DOCUMENTO,NUM_DOCUMENTO,PRI_APELLIDO,SEG_APELLIDO,PRI_NOMBRE,SEG_NOMBRE,COD_TIPO_AFIl,FEC_AFILIACION_UNI,COD_REGIONAL
FROM AFILIADOS
WHERE tip_documento +num_documento NOT IN (select DET_PLANILLA_AUTOLIQUIDACION.AFI_TIPO_DOC+DET_PLANILLA_AUTOLIQUIDACION.AFI_NUMERO_DOC FROM det_planilla_autoliquidacion where  AFILIADOS.FEC_AFILIACION_UNI <  "2001/01/01"  AND AFILIADOS.COD_TIPO_AFIL= 'C' ) 
ORDER  BY COD_REGIONAL 







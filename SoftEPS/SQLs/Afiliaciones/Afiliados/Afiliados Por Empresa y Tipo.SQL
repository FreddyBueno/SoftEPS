SELECT NOM_EMPRESA,NUM_DOCUMENTO_EMP, COD_TIPO_AFIL,COUNT(*) FROM AFILIADOS,RELACION_AFILIADOS,AFILIADOS_EMPRESAS,EMPRESAS
WHERE AFILIADOS.TIP_DOCUMENTO = TIP_DOCUMENTO_BEN
AND AFILIADOS.NUM_DOCUMENTO = NUM_DOCUMENTO_BEN
AND TIP_DOCUMENTO_COT = TIP_DOCUMENTO_AFI
AND NUM_DOCUMENTO_COT = NUM_DOCUMENTO_AFI
AND TIP_DOCUMENTO_EMP = EMPRESAS.TIP_DOCUMENTO
AND NUM_DOCUMENTO_EMP = EMPRESAS.NUM_DOCUMENTO
GROUP BY NOM_EMPRESA,NUM_DOCUMENTO_EMP, COD_TIPO_AFIL
